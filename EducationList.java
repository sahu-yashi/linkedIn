public class EducationList {
    String school;
    String degree;
    String fieldOfStudy;
    String grade;
    String description;
    public EducationList(String school, String degree, String fieldOfStudy, String grade, String description) {
        this.school = school;
        this.degree = degree;
        this.fieldOfStudy = fieldOfStudy;
        this.grade = grade;
        this.description = description;
        
    }
}
    
    
